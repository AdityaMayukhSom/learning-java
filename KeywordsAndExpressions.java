public class KeywordsAndExpressions {
    public static void main(String[] args) {
        // Java has 53 keywords
        // Expressions are building blocks of Java programs

        // A mile is equal to 1.609344 kilometres
        double miles = 100;
        double MILES_TO_KILOMETRES = 1.609344;
        double kilometres = MILES_TO_KILOMETRES * miles;
        System.out.println(kilometres);

        int highScore = 100;
        if (highScore == 100) {
            System.out.println("This is an expression");
        }

    }
}
