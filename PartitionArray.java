    Map<List<Integer>, Integer> cache = new HashMap<>();
    
    public int valueSum(int[] nums, int[] andValues) {
        int val = minValueSum(nums, andValues, 0, 0);
        if(val == 1000000010) {
            return -1;
        }
        return val;
    }
    
    public int minValueSum(int[] nums, int[] andValues, int a, int b) {
        if(a == nums.length && b == andValues.length) {
            return 0;
        }
        else if(a >= nums.length || b >= andValues.length) {
            return 1000000010;
        }
        
        List<Integer> key = List.of(a,b);
        if(cache.containsKey(key)) {
            return cache.get(key);
        }

        int res = 1000000010;
        int curAndValue = nums[a];
        int minSoFar = 1000000010;

        for(int i = a; i < nums.length && curAndValue >= andValues[b]; i++) {
            curAndValue = curAndValue & nums[i];
            if(curAndValue == andValues[b]) {
                if(nums[i] < minSoFar) {
                    int recurseResult = minValueSum(nums, andValues, i+1, b+1);
                    if(recurseResult != 1000000010) {
                        res = Math.min(res, nums[i]+recurseResult);
                        minSoFar = Math.min(minSoFar, nums[i]);
                    }
                }
            }
        }
        
        cache.put(key, res);
        return res;
    }

    private int solve(final int[] nums, final int[] andValues, int start, int partition, int[][] dp){      
        int n = nums.length;
        int m = andValues.length;
         
        if(start == -1 && partition == -1) return 0;
        if(start < 0 || partition < 0) return Integer.MAX_VALUE;
        // if(partition == 0){
        //     int v = Integer.MAX_VALUE;
        //     for(int i = 0; i <= start; i++) v = v & nums[i];
        //     return dp[start][partition] = (v == andValues[0]) ? nums[start] : Integer.MAX_VALUE;
        // }

        if(dp[start][partition] != -1) {
            return dp[start][partition];
        }
        
        int cost = Integer.MAX_VALUE;
        int biti = Integer.MAX_VALUE;
        int minSoFar = Integer.MAX_VALUE;
         
        for(int i = start; i >= 0 && biti >= andValues[partition]; i--){
            biti = biti & nums[i];
            if(biti == andValues[partition]){
                if(minSoFar > nums[start]){
                    // System.out.println("m3");

                    // two choices, either continue same partition or create a new partition
                    // to continue with the same, we don't have to do much either, just let
                    // the loop continue in itself
                    
                    // create a new partition
                    int a1 = solve(nums, andValues, i - 1, partition - 1, dp);
                    if(a1 != Integer.MAX_VALUE){
                        cost = Math.min(a1 + nums[start], cost);
                        // minSoFar = Math.min(minSoFar, nums[i]);
                    }
                }
            }
            
        }
        return dp[start][partition] = cost;
    }
    
    
    public int minimumValueSum(int[] nums, int[] andValues) {
        int n = nums.length;
        int m = andValues.length;
        
        int[][] dp = new int[n][m];
        for(int[] row: dp) Arrays.fill(row, -1);
        
        int ans = solve(nums, andValues, n - 1, m - 1, dp);
        return ans == Integer.MAX_VALUE ? -1: ans;
    }
    


